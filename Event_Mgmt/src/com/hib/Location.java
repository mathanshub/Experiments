package com.hib;

public class Location {

	private int loc_id;
	private String loc_name;
	private String loc_addrs;

	public int getLoc_id() {
		return loc_id;
	}

	public void setLoc_id(int loc_id) {
		this.loc_id = loc_id;
	}

	public String getLoc_name() {
		return loc_name;
	}

	public void setLoc_name(String loc_name) {
		this.loc_name = loc_name;
	}

	public String getLoc_addrs() {
		return loc_addrs;
	}

	public void setLoc_addrs(String loc_addrs) {
		this.loc_addrs = loc_addrs;
	}

}
